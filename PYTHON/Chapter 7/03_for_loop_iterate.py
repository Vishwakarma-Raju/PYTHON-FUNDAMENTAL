l = ["raju",2,"rajjo",8,9,False]
for i in l:
    print(i)

#for loop with tuple
t = (8,9,"raju",98,"jojo",143,False) 

for i in t:
    print(i)


#for loop with str
s = "Raju Vishwakarma"

for i in s:
    print(i)

